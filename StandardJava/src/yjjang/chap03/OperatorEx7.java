package yjjang.chap03;
class OperatorEx7 {
	public static void main(String[] args) {
		boolean power = false;
		System.out.println(power);
		power = !power;		         // power의 값이 false에서 true로 바뀐다.
		System.out.println(power);
		power = !power;		         // power의 값이 True에서 false로 바뀐다.
		System.out.println(power);
	}
}